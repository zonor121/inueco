# операторы сравнения
"""
> - больше
< - меньше
>= - больше или равно
<= - меньше или равно
!= - не равно
== - равно
"""
# сравнение, возвращает True или False, True - истина, False - ложь
# логическое выражение
print('x > y', x > y)
print('x < y', x < y)
print('x >= y', x >= y)
print('x <= y', x <= y)
print('x != y', x != y)
print('x == y', x == y)
# логические операторы
"""
and - и *
or - или +
not - не, отрицание, инверсия
"""
print('x > y and x < y', x > y and x < y)
print('x > y or x < y', x > y or x < y)
print('not x > y', not x > y)

